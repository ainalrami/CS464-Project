#!/usr/bin/env python3
"""
Results Summarization Script
==============================

Usage:
    python summarize_results.py [--results-dir ./results]

Aggregates all metrics from results/metrics/ into final comparison
tables and generates publication-style figures.

Outputs
-------
results/metrics/
    overall_test_summary.csv      — all models ranked by test accuracy
    upscaling_comparison.csv      — native vs upscaled ML comparison
    robustness_pivot.csv          — models × degradation conditions

results/plots/
    model_comparison.png          — accuracy + F1 bar chart (all models)
    per_class_f1_comparison.png   — per-class F1 grouped bar chart
    upscaling_comparison.png      — native vs upscaled accuracy per model
    robustness_comparison.png     — already generated by run_robustness.py
    (training curves already generated per model by run_dl.py)
"""

import argparse
import logging
import sys
from pathlib import Path

import matplotlib
matplotlib.use("Agg")
import matplotlib.pyplot as plt
import numpy as np
import pandas as pd

from src.evaluation.plots import plot_model_comparison_bar, plot_per_class_f1

logging.basicConfig(
    level=logging.INFO,
    format="%(asctime)s [%(levelname)s] %(name)s: %(message)s",
    handlers=[logging.StreamHandler(sys.stdout)],
)
logger = logging.getLogger("summarize")

plt.rcParams.update({
    "figure.facecolor": "white",
    "axes.facecolor": "white",
    "axes.grid": True,
    "grid.alpha": 0.3,
    "font.size": 11,
})


def plot_upscaling_comparison(upscale_df, output_path):
    """Bar chart: native 64×64 vs upscaled 128×128 accuracy per model."""
    if upscale_df.empty:
        return

    output_path = Path(output_path)
    output_path.parent.mkdir(parents=True, exist_ok=True)

    native = upscale_df[upscale_df["size_label"] == "native"].set_index("model_feat")
    upscaled = upscale_df[upscale_df["size_label"] == "upscaled"].set_index("model_feat")
    common = native.index.intersection(upscaled.index)
    if common.empty:
        logger.warning("No matching native/upscaled pairs found, skipping upscaling plot.")
        return

    labels = list(common)
    x = np.arange(len(labels))
    width = 0.35

    fig, ax = plt.subplots(figsize=(max(10, len(labels) * 1.2), 5))
    ax.bar(x - width / 2, native.loc[labels, "test_accuracy"], width,
           label="Native 64×64", color="#4C72B0")
    ax.bar(x + width / 2, upscaled.loc[labels, "test_accuracy"], width,
           label="Upscaled 128×128", color="#DD8452")

    ax.set_xticks(x)
    ax.set_xticklabels(labels, rotation=30, ha="right")
    ax.set_ylabel("Test Accuracy")
    ax.set_title("Upscaling Effect: Native 64×64 vs Upscaled 128×128")
    ax.legend()
    ax.set_ylim(0, 1.05)
    plt.tight_layout()
    fig.savefig(output_path, dpi=150)
    plt.close(fig)
    logger.info(f"  Upscaling comparison plot saved to {output_path}")


def main():
    parser = argparse.ArgumentParser(description="Summarize ML/DL pipeline results.")
    parser.add_argument("--results-dir", type=str, default="./results")
    args = parser.parse_args()

    results_dir = Path(args.results_dir)
    metrics_dir = results_dir / "metrics"
    plots_dir = results_dir / "plots"
    plots_dir.mkdir(parents=True, exist_ok=True)

    if not metrics_dir.exists():
        logger.error("No results found. Run the ML, DL, and robustness pipelines first.")
        sys.exit(1)

    # ----------------------------------------------------------------
    # 1. Aggregate ALL test-set metrics (ML + DL)
    # ----------------------------------------------------------------
    test_files = sorted(metrics_dir.glob("*_test_metrics.csv"))
    if not test_files:
        logger.warning("No *_test_metrics.csv files found.")

    all_rows = []
    for f in test_files:
        df = pd.read_csv(f)
        all_rows.append(df)

    if all_rows:
        test_summary = pd.concat(all_rows, ignore_index=True).drop_duplicates(
            subset=["model"], keep="last"
        ).sort_values("accuracy", ascending=False).reset_index(drop=True)

        summary_path = metrics_dir / "overall_test_summary.csv"
        test_summary.to_csv(summary_path, index=False)

        logger.info("\n" + "=" * 60)
        logger.info("OVERALL TEST SET PERFORMANCE")
        logger.info("=" * 60)
        logger.info(f"\n{test_summary.to_string(index=False)}")

        # Model comparison bar chart
        if "accuracy" in test_summary.columns and "macro_f1" in test_summary.columns:
            plot_model_comparison_bar(test_summary, plots_dir / "model_comparison.png")

    # ----------------------------------------------------------------
    # 2. Per-class F1 comparison (all models that have a test report)
    # ----------------------------------------------------------------
    report_files = sorted(metrics_dir.glob("*_test_report.csv"))
    per_class_results = {}
    exclude_rows = {"accuracy", "macro avg", "weighted avg"}

    for f in report_files:
        model_name = f.stem.replace("_test_report", "")
        df = pd.read_csv(f, index_col=0)
        per_class = {
            idx: float(df.loc[idx, "f1-score"])
            for idx in df.index
            if idx not in exclude_rows and "f1-score" in df.columns
        }
        if per_class:
            per_class_results[model_name] = per_class

    if per_class_results:
        first = list(per_class_results.values())[0]
        class_names = list(first.keys())
        plot_per_class_f1(per_class_results, class_names,
                          plots_dir / "per_class_f1_comparison.png")
        logger.info(f"  Per-class F1 plot saved ({len(per_class_results)} models).")

    # ----------------------------------------------------------------
    # 3. Upscaling comparison
    # ----------------------------------------------------------------
    ml_summary_path = metrics_dir / "ml_summary.csv"
    if ml_summary_path.exists():
        ml_df = pd.read_csv(ml_summary_path)

        if "size_label" in ml_df.columns and "feature_mode" in ml_df.columns:
            ml_df["model_feat"] = ml_df["model"] + "_" + ml_df["feature_mode"]
            upscale_subset = ml_df[["model_feat", "size_label", "test_accuracy", "test_f1"]]
            upscale_path = metrics_dir / "upscaling_comparison.csv"

            # Pivot for easy reading
            try:
                pivot = ml_df.pivot_table(
                    index=["model", "feature_mode"],
                    columns="size_label",
                    values=["test_accuracy", "test_f1"],
                    aggfunc="first",
                )
                pivot.columns = ["_".join(c) for c in pivot.columns]
                pivot = pivot.reset_index()
                if "test_accuracy_native" in pivot.columns and "test_accuracy_upscaled" in pivot.columns:
                    pivot["accuracy_delta"] = (
                        pivot["test_accuracy_upscaled"] - pivot["test_accuracy_native"]
                    ).round(4)
                pivot.to_csv(upscale_path, index=False)
                logger.info("\n" + "=" * 60)
                logger.info("UPSCALING COMPARISON (64×64 vs 128×128)")
                logger.info("=" * 60)
                logger.info(f"\n{pivot.to_string(index=False)}")
            except Exception as e:
                logger.warning(f"Could not build upscaling pivot: {e}")

            plot_upscaling_comparison(upscale_subset, plots_dir / "upscaling_comparison.png")

    # ----------------------------------------------------------------
    # 4. ML summary
    # ----------------------------------------------------------------
    if ml_summary_path.exists():
        ml_df = pd.read_csv(ml_summary_path)
        logger.info("\n" + "=" * 60)
        logger.info("ML PIPELINE SUMMARY")
        logger.info("=" * 60)
        logger.info(f"\n{ml_df.to_string(index=False)}")

    # ----------------------------------------------------------------
    # 5. DL summary
    # ----------------------------------------------------------------
    dl_summary_path = metrics_dir / "dl_summary.csv"
    if dl_summary_path.exists():
        dl_df = pd.read_csv(dl_summary_path)
        logger.info("\n" + "=" * 60)
        logger.info("DL PIPELINE SUMMARY")
        logger.info("=" * 60)
        logger.info(f"\n{dl_df.to_string(index=False)}")

    # ----------------------------------------------------------------
    # 6. Robustness summary + pivot
    # ----------------------------------------------------------------
    robustness_file = metrics_dir / "robustness_results.csv"
    if robustness_file.exists():
        rob_df = pd.read_csv(robustness_file)

        logger.info("\n" + "=" * 60)
        logger.info("ROBUSTNESS ANALYSIS RESULTS")
        logger.info("=" * 60)
        logger.info(f"\n{rob_df.to_string(index=False)}")

        try:
            pivot = rob_df.pivot_table(
                index="model", columns="condition",
                values="accuracy", aggfunc="first",
            )
            # Reorder columns: clean first, then degradations
            cols = [c for c in ["clean"] if c in pivot.columns]
            cols += [c for c in pivot.columns if c != "clean"]
            pivot = pivot[cols]

            pivot_path = metrics_dir / "robustness_pivot.csv"
            pivot.to_csv(pivot_path)
            logger.info(f"\nRobustness pivot saved to {pivot_path}")
            logger.info(f"\n{pivot.to_string()}")
        except Exception as e:
            logger.warning(f"Could not build robustness pivot: {e}")
    else:
        logger.info("No robustness results found. Run run_robustness.py first.")

    logger.info(f"\n{'=' * 60}")
    logger.info("Summarization complete!")
    logger.info(f"{'=' * 60}")
    logger.info(f"\nPlots  → {plots_dir}/")
    logger.info(f"Tables → {metrics_dir}/")


if __name__ == "__main__":
    main()
